func convertToTitle(columnNumber int) string {
	//-------------------------------æ¨¡æ‹Ÿ--------------------------------
	// Time: O(n)
	// Space: O(n)
	ans := []byte{}
	for columnNumber != 0 {
		columnNumber -= 1
		tem := columnNumber % 26
		ans = append(ans , byte(tem + 'A'))
		columnNumber /= 26
	}
	for i := 0 ; i < len(ans) / 2 ; i++ {
		ans[i], ans[len(ans) - i - 1] = ans[len(ans) - i - 1], ans[i]
	}
	return string(ans)
	//-------------------------------------------------------------------
}
