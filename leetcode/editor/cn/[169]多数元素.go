func majorityElement(nums []int) int {
	//---------------------------------æ’åº-------------------------------
	/*// Time: O(nlogn)
	// Space: O(logn)
	sort.Ints(nums)
	return nums[len(nums) / 2]*/
	//--------------------------------------------------------------------

	//-------------------------------æ‘©å°”æŠ•ç¥¨-------------------------------
	// Time: O(n)
	// Space: O(1)
	vote, candidate := 0, 0
	for _, v := range nums {
		if vote == 0 {
			candidate = v
		}
		if v == candidate {
			vote++
		}else{
			vote--
		}
	}
	return candidate
	//--------------------------------------------------------------------
}
