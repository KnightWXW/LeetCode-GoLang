/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func levelOrder(root *TreeNode) [][]int {
	//-------------------------------队列----------------------------------
	// Time: O(n)
	// Space: O(n)
	ans := [][]int{}
	if root == nil {
		return ans
	}
	queue := []*TreeNode{root}
	for len(queue) > 0 {
		size := len(queue)
		tem := []int{}
		for i := 0 ; i < size ; i++ {
			cur := queue[0]
			queue = queue[1 : ]
			tem = append(tem, cur.Val)
			if cur.Left != nil {
				queue = append(queue, cur.Left)
			}
			if cur.Right != nil {
				queue = append(queue, cur.Right)
			}
		}
		ans = append(ans, tem)
	}
	return ans
	//-----------------------------------------------------------------
}
