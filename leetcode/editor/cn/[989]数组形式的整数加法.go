func addToArrayForm(num []int, k int) []int {
	//------------------------------------æ¨¡æ‹Ÿ----------------------------------
	// Time: O(max(n,logk))
	// Space: O(1)
	ans := []int{}
	for i := len(num) - 1 ; i >= 0 || k > 0 ; i-- {
		if i >= 0 {
			k += num[i]
		}
		ans = append(ans , k % 10)
		k /= 10
	}
	reverse(ans)
	return ans
	//-------------------------------------------------------------------------
}

func reverse(arr []int) {
	i , j := 0 , len(arr) - 1
	for i < j {
		arr[i], arr[j] = arr[j], arr[i]
		i++
		j--
	}
}
